package com.jtanks.controller;

public class TrackedPoint {
    private double x;
    private double y;
    
    public TrackedPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }
    
    public double getY() {
        return y;
    }
}
